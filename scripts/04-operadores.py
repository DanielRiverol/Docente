# Operadores
# Aritmeticos + - * / %
print(4 + 5)
print(2 - 4)
print(12 * 4)
print(22 / 4)
print(21 % 2)


# asignación
variable = 3
print(variable)
# variable = variable + 4
variable += 4
print(variable)


# comparación == != < <= > >=
print(10 == 10)
print(10 == "10")
print("hola" != "HOLA")
print(4 != 4)
print(4 < 4)
print(4 <= 4)
print(4 > 4)
print(4 >= 4)

# logicos and or not
print(10 > 3 or 4 < 6)
print(10 == 3 and 4 < 6)
