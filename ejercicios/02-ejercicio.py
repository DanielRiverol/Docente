"""
Un avión viaja 395000 metros en 9000 segundos.
Escribe un programa que calcule la velocidad
Formula: velocidad= distancia/tiempo
"""

# input
distancia = 395000
tiempo = 9000
# solucion
velocidad = distancia / tiempo
# output
print(
    f"La velocidad a la que viaja un avión que recorre {distancia} metros en {tiempo} segundos es: {velocidad:.2f} m/s"
)
